     * @return \Illuminate\Http\Response
     */
    public function index(Request $request, WeddingAlbum $wedding_album)
    {
        $invited_users = WeddingAlbum::find($wedding_album->id)->users()->get();

        if ($request->keyword !== null) {
            $keyword = rtrim($request->keyword);
            if (is_int($request->keyword)) {
                $keyword = (string)$keyword;
            }
            $users = User::where('name', 'like', "%{$keyword}%")
                            ->orwhere('email', 'like', "%{$keyword}%")
                            ->orwhere('id', "%{$keyword}%")
                            ->paginate(15);
        } else {
            $users = [];
            $keyword = "";
        }

        return view('dashboard.wedding_albums.invite', compact('wedding_album', 'users', 'invited_users', 'keyword'));
    }

